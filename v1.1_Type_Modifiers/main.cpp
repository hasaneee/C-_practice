#include <iostream>

using namespace std;

int main(){
    int i;
    cout << "Size of int type: " << sizeof(i) <<endl;

    float j;
    cout << "Size of float type: " << sizeof(j) <<endl;

    double k;
    cout << "Size of double type: " << sizeof(k) <<endl;

    char c;
    cout << "Size of character type: " << sizeof(c) <<endl;

    long int x;
    cout << "Size of long int type: " << sizeof(x) <<endl;

    short int y;
    cout << "Size of short int type: " << sizeof(y) <<endl;

    return 0;
}